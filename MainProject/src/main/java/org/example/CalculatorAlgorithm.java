package org.example;

public class CalculatorAlgorithm {
    public int plus(int n1, int n2) {
        return n1 + n2;
    }
    public int minus(int n1, int n2){
        return n1-n2;
    }
    public int mul(int n1, int n2){
        return n1*n2;
    }
    public int div(int n1, int n2){
        return n1/n2;
    }
}
